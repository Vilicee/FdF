#include "../includes/fdf.h"

int	ft_clip(t_data *data)
{
	if (data->y - data->offset <= 0 || data->y - data->offset >= 1000)
		return (-1);
	if (data->x <= 0 || data->x >= 1600)
		return (-1);
	return (1);
}
